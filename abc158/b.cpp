#pragma region Debug
template <typename T>
void view(const std::vector<T> &v)
{
    for (const auto &e : v)
    {
        std::cout << e << " ";
    }
    std::cout << std::endl;
}
template <typename T>
void view(const std::vector<std::vector<T>> &vv)
{
    for (const auto &v : vv)
    {
        view(v);
    }
}
#pragma endregion

int main()
{
    ll n, a, b;
    cin >> n >> a >> b;
    ll rem = n % (a + b);
    ll q = n / (a + b);
    ll ans = (q * a) + min(rem, a);

    cout << ans << endl;
    return 0;
}